// Installs NPM dependencies for an example.
//
// This is used as a setup routine for examples.

import { execFileSync } from "node:child_process";
import { test } from "#tests/example_project_test/client_test_options.js";

test("install node modules", async ({ clientDir }) => {
  test.slow();
  // Use execFileSync because it supports an `stdio` option.
  execFileSync("pnpm", ["install", "--frozen-lockfile"], {
    cwd: clientDir,
    stdio: ["ignore", "inherit", "inherit"],
  });
});
